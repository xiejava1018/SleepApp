package com.doit8.sleep.pojo;

public class SelectedItem {
	private String topicId;
	private int score;
	private int selectted;
	
	public String getTopicId() {
		return topicId;
	}
	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}

	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getSelectted() {
		return selectted;
	}
	public void setSelectted(int selectted) {
		this.selectted = selectted;
	}

	

}
